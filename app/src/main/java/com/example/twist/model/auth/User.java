package com.example.twist.model.auth;

public class User {
    private int id;
    private String username;
    private String email;
    private String createdAt;

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public String getCreatedAt() {
        return createdAt;
    }
}
