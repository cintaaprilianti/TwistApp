package com.example.twist.model.response;

public class User{
	private String createdAt;
	private int id;
	private String email;
	private String username;

	public String getCreatedAt(){
		return createdAt;
	}

	public int getId(){
		return id;
	}

	public String getEmail(){
		return email;
	}

	public String getUsername(){
		return username;
	}
}
